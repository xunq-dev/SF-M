/** Apply all V3 theme tokens as CSS custom properties on `:root`. */
export function applyV3ThemeToDocument(theme: V3ThemeState): void {
  const root = document.documentElement;
  const vars = v3ThemeInlineVars(theme);
  for (const [key, value] of Object.entries(vars)) {
    if (key.startsWith("--")) root.style.setProperty(key, value);
  }
  root.style.fontFamily = getUiFontStack(theme.typography.uiFontId);
  const step = theme.typography.uiFontSizeStep;
  if (step <= 0) root.style.removeProperty("--v3-ui-font-step");
}
