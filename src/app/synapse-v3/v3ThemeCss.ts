    "--v3-editor-work-bg": editor.workAreaBg,
    "--v3-editor-tabbar-bg": editor.tabBarBg,
    "--v3-editor-tab-inactive": editor.tabInactiveBg,
    "--v3-editor-tab-active": editor.tabActiveBg,
    "--v3-editor-tab-text": editor.tabText,
    "--v3-editor-tab-border": editor.tabBorder,
    "--v3-script-section-bg": scriptList.sectionHeaderBg,
    "--v3-script-section-text": scriptList.sectionHeaderText,
    "--v3-script-section-icon": scriptList.sectionIcon,
    "--v3-script-search-bg": scriptList.searchBg,
    "--v3-script-search-placeholder": scriptList.searchPlaceholder,
    "--v3-script-row-text": scriptList.rowText,
